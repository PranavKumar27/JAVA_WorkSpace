import java.util.*;
import java.lang.*;

class DataType
{
	public static void main(String args[])
	{
		System.out.println("Byte Max_val = " + Byte.MAX_VALUE);
		System.out.println("Byte Min val =" + Byte.MIN_VALUE);
		System.out.println("Byte in BYTE = " + Byte.BYTES);
		
		System.out.println("Integer Max_val = " + Integer.MAX_VALUE);
		System.out.println("Integer Min val =" + Integer.MIN_VALUE);
		System.out.println("Integer in BYTE = " + Integer.BYTES);
		
		System.out.println("Float Max_val = " + Float.MAX_VALUE);
		System.out.println("Float Min val =" + Float.MIN_VALUE);
		System.out.println("Float in BYTE = " + Float.BYTES);
		
		System.out.println("Character Max_val = " + Character.MAX_VALUE);
		System.out.println("Character Min val =" + Character.MIN_VALUE);
		System.out.println("Character in BYTE = " + Character.BYTES);
	
		System.out.println("Boolean Max_val = " + Boolean.TRUE);
		System.out.println("Boolean Min val =" + Boolean.FALSE);
		System.out.println("Boolean in BYTE = " + Boolean.TYPE);
	}
}
